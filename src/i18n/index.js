function getValue(path, dictionary) {
  return path.split('.').reduce((current, segment) => current?.[segment], dictionary);
}

function interpolate(template, variables) {
  if (typeof template !== 'string') return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => `${variables[key] ?? ''}`);
}

export function createI18n(initialLanguage = 'en') {
  let currentLanguage = translations[initialLanguage] ? initialLanguage : 'en';

  return {
    get language() {
      return currentLanguage;
    },
    get languages() {
      return LANGUAGE_OPTIONS;
    },
    setLanguage(nextLanguage) {
      currentLanguage = translations[nextLanguage] ? nextLanguage : 'en';
    },
    t(path, variables = {}) {
      const value = getValue(path, translations[currentLanguage]) ?? getValue(path, translations.en) ?? path;
      return interpolate(value, variables);
    },
    getDictionary() {
      return translations[currentLanguage];
    },
  };
}
