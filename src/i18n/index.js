function mergeDeep(baseLanguage, overrides) {
  const merged = { ...baseLanguage };

  Object.entries(overrides).forEach(([key, value]) => {
    if (Array.isArray(value)) {
      merged[key] = value;
      return;
    }

    if (value && typeof value === 'object') {
      merged[key] = mergeDeep(baseLanguage[key] ?? {}, value);
      return;
    }

    merged[key] = value;
  });

  return merged;
}

